CREATE FUNCTION dbo.MathMax(
	@Val1 DECIMAL(38,5)= -999999999999999999999999999999999.99999,
	@Val2 DECIMAL(38,5)= -999999999999999999999999999999999.99999,
	@Val3 DECIMAL(38,5)= -999999999999999999999999999999999.99999,
	@Val4 DECIMAL(38,5)= -999999999999999999999999999999999.99999,
	@Val5 DECIMAL(38,5)= -999999999999999999999999999999999.99999
)
RETURNS DECIMAL(38,5)
AS 
BEGIN 
DECLARE @OutPut DECIMAL(38,5) 

SELECT @OutPut =
	CASE 
		WHEN @Val1 >= @Val2 AND @Val1 >= @Val3 AND @Val1 >= @Val4 AND @Val1 >= @Val5 THEN @Val1
		WHEN @Val2 >= @Val1 AND @Val2 >= @Val3 AND @Val2 >= @Val4 AND @Val2 >= @Val5 THEN @Val2
		WHEN @Val3 >= @Val1 AND @Val3 >= @Val2 AND @Val3 >= @Val4 AND @Val3 >= @Val5 THEN @Val3
		WHEN @Val4 >= @Val1 AND @Val4 >= @Val2 AND @Val4 >= @Val3 AND @Val4 >= @Val5 THEN @Val4
		WHEN @Val5 >= @Val1 AND @Val5 >= @Val2 AND @Val5 >= @Val3 AND @Val5 >= @Val4 THEN @Val5
	END
RETURN @OutPut
END